#include <math.h>
#include "functions.h"

double add(double x, double y) {
	return x + y;
};

double sub(double x, double y) {
	return x - y;
};

double mult(double x, double y) {
	return x * y;
};

double divide(double x, double y) {
	return x / y;
};

double power(double x, double y) {
	return pow(x, y);
};